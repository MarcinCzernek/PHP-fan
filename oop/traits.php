<h2>Przestrzenie nazw</h2>
<p>Przestrzenie nazw (z ang. Namespace) umożliwiają zadeklarowanie wielu klas o takiej samej nazwie, wprowadzają też swego rodzaju porządek i grupowanie w projekcie.</p>
<p>Deklarujemy ją jako pierwszą instrukcje w pliku php przy użyciu nazwy namespace.</p>
<code>namespace naukaPHP;</code>
<p>Możliwe też jest zadeklarowanie podprzestrzeni.</p>
    <code>namespace naukaPHP\index;</code>
